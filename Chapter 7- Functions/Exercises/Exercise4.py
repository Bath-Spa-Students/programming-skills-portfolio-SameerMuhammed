# Modify the make_shirt() function

def make_shirt(size="large", message="I love Python"):
    print(f"Making a {size}-sized shirt with the message: '{message}'.")

# Call the function with default values (large size and default message)
make_shirt()

# Call the function with medium size and default message
make_shirt("medium")

# Call the function with a specific size and a different message
make_shirt(size="small", message="Keep coding!")
